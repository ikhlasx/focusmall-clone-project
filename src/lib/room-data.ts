// Room data structure based on the booking lists

export type RoomStatus = "Vacant" | "Confirmed" | "Rented";

export type RoomType = "Shop Room" | "Business Centre" | "Food Court" | "Gym" | "Super Market";

export interface ShopRoom {
  id: string;
  number: number | string;
  name: string;
  regNo: string;
  shopDetails: string;
  rentPerSqFt: number;
  status: RoomStatus;
  location: string;
  remarks?: string;
  block: "A Block" | "B Block";
  floor?: string;
  imageUrl?: string;
  floorImageUrl?: string;
}

export interface BusinessCentreRoom {
  id: string;
  number: number | string;
  name: string;
  rate: number;
  type: "MINI ROOMS" | "LARGE ROOMS" | "DAILY WORK SPACE" | "WEEKLY WORK SPACE" | "WORK SPACE MONTHLY" | "S.P WORK SPACE";
  quantity: number;
  totalAmount: number;
  location: string;
  remarks?: string;
  block: "A Block" | "B Block";
  status: RoomStatus;
  imageUrl?: string;
  floorImageUrl?: string;
}

export type Room = ShopRoom | BusinessCentreRoom;

