// Helper functions
export function isShopRoom(room: Room): room is ShopRoom {
  return "rentPerSqFt" in room && "shopDetails" in room;
}

export function isBusinessCentreRoom(room: Room): room is BusinessCentreRoom {
  return "type" in room && "rate" in room && "quantity" in room;
}

